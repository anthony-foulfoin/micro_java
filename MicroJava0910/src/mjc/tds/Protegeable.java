package mjc.tds;

/**
 * Interface Protegeable.
 * 
 * Caracterise des classes ayant une protection public ou private.
 * 
 * @author jdalbert
 *
 */
public interface Protegeable {
	
	/**
	 * @return true si la protection est public, false sinon
	 */
	public Boolean estPublic();
	

	/**
	 * @param estPublic true pour une protection public,
	 * 					false pour une protection private
	 */
	public void setProtection(Boolean estPublic);
	
}
